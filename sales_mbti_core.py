def ask_questions(questions_data):
    answers = {}
    indexed_questions_data = []
    for idx, q in enumerate(questions_data):
        indexed_questions_data.append({"data": q, "original_index": idx})

    print("영업 성향 진단 테스트를 시작합니다. 각 질문에 대해 가장 가까운 답변을 선택해주세요.\n")
    
    questions_by_axis_group = {1: [], 2: [], 3: [], 4: []}
    for item in indexed_questions_data:
        questions_by_axis_group[item["data"]["axis"]].append(item)

    for i in range(1, 5): 
        axis_label = ""
        if i == 1: axis_label = "Axis 1: P (Proactive) vs R (Reactive) - 접근 방식"
        elif i == 2: axis_label = "Axis 2: S (Solution-focused) vs F (Product-focused) - 가치 전달 방식"
        elif i == 3: axis_label = "Axis 3: E (Emotional) vs T (Transactional) - 관계 구축 방식"
        elif i == 4: axis_label = "Axis 4: I (Independent) vs C (Collaborative) - 업무 스타일"
        print(f"\n--- {axis_label} ---")
        
        axis_answers_for_current_group = []
        for q_item in questions_by_axis_group[i]:
            q_data = q_item["data"]
            print(f"\n{q_data['text']}")
            for opt_idx, option in enumerate(q_data["options"]):
                print(option)
            
            while True:
                try:
                    answer = input(f"답변 (1-{len(q_data['options'])}): ")
                    answer_num = int(answer)
                    if 1 <= answer_num <= len(q_data['options']):
                        axis_answers_for_current_group.append({
                            "question_index": q_item["original_index"],
                            "chosen_option_index": answer_num - 1
                        })
                        break
                    else:
                        print(f"잘못된 입력입니다. 1에서 {len(q_data['options'])} 사이의 숫자를 입력해주세요.")
                except ValueError:
                    print("잘못된 입력입니다. 숫자를 입력해주세요.")
        answers[i] = axis_answers_for_current_group
    return answers


def calculate_scores(answers_data, questions_data_main_list):
    axis_scores = {1: {'P': 0, 'R': 0}, 2: {'S': 0, 'F': 0}, 3: {'E': 0, 'T': 0}, 4: {'I': 0, 'C': 0}}
    poles_map = {1: ('P', 'R'), 2: ('S', 'F'), 3: ('E', 'T'), 4: ('I', 'C')}

    for axis_group_number, list_of_answers_for_this_group in answers_data.items():
        for ans_detail in list_of_answers_for_this_group:
            question_proper = questions_data_main_list[ans_detail["question_index"]]
            chosen_option_idx = ans_detail["chosen_option_index"]
            
            actual_axis_of_question = question_proper["axis"]
            current_axis_poles = poles_map[actual_axis_of_question]
            pole1_char = current_axis_poles[0]
            pole2_char = current_axis_poles[1]
            
            score_values_for_pole1 = question_proper["scores"].get(pole1_char, [0]*len(question_proper["options"]))
            score_values_for_pole2 = question_proper["scores"].get(pole2_char, [0]*len(question_proper["options"]))
            
            axis_scores[actual_axis_of_question][pole1_char] += score_values_for_pole1[chosen_option_idx]
            axis_scores[actual_axis_of_question][pole2_char] += score_values_for_pole2[chosen_option_idx]
            
    personality_code = ""
    if axis_scores[1]['P'] >= axis_scores[1]['R']: personality_code += 'P'
    else: personality_code += 'R'

    if axis_scores[2]['S'] >= axis_scores[2]['F']: personality_code += 'S'
    else: personality_code += 'F'

    if axis_scores[3]['E'] >= axis_scores[3]['T']: personality_code += 'E'
    else: personality_code += 'T'
        
    if axis_scores[4]['I'] >= axis_scores[4]['C']: personality_code += 'I'
    else: personality_code += 'C'
        
    return personality_code

